// ============================================================================
// Benchmark report shapes — the structured result of one competitor-benchmark
// run, stored in benchmark_runs.result and rendered by the premium dashboard.
//
// Everything here is derived from FREE data sources (Google Keyword Planner +
// fetching public landing pages) EXCEPT `CompetitorAd[]`, which comes from the
// optional, admin-gated PAID ad-spy block. When the gate is off, `ads` is null
// and `adsStatus` is "off" — the rest of the report is fully populated.
// ============================================================================

export type Competition =
  | "LOW"
  | "MEDIUM"
  | "HIGH"
  | "UNSPECIFIED"
  | "UNKNOWN";

/** One keyword with its real Google Keyword Planner metrics. */
export interface BenchmarkKeyword {
  text: string;
  avgMonthlySearches: number;
  competition: Competition;
  cpcLowMicros?: number;
  cpcHighMicros?: number;
}

/** A tracking/analytics stack detected on a competitor's landing page. */
export interface TrackingStack {
  /** Distinct utm_* params found on outbound/internal links (key→sample value). */
  utmParams: { key: string; value: string }[];
  /** Detected pixels/tags (GA4, Google Ads, GTM, Meta Pixel, TikTok, …). */
  pixels: string[];
  hasGtm: boolean;
  gtmIds: string[];
  gaIds: string[];
  adsConversionIds: string[];
}

/** LLM teardown of a competitor's landing page (the "what they say" layer). */
export interface CompetitorLanding {
  url: string;
  httpStatus: number | null;
  title: string;
  valueProposition: string;
  offers: string[];
  ctas: string[];
  trustSignals: string[];
  toneNotes: string;
  tracking: TrackingStack;
}

/** One transparency-center ad creative (PAID ad-spy only; null when gated off). */
export interface CompetitorAd {
  format: string; // text | image | video
  headline?: string;
  body?: string;
  destinationUrl?: string;
  firstShown?: string;
  lastShown?: string;
}

export type AdsStatus = "off" | "empty" | "ok" | "error";

/** One keyword in a competitor's modeled spend, with its contribution. */
export interface SpendKeyword {
  text: string;
  avgMonthlySearches: number;
  cpcMicros: number; // mid top-of-page CPC (micros) — reuses the CPC formatter
  estMonthlyClicks: number;
  estMonthlyMid: number; // modeled monthly spend on this keyword, currency units
}

/**
 * Modeled estimate of how much a competitor invests in Google Search per month.
 * Nobody can see a competitor's real spend — this is inferred the same way the
 * big tools do it: real Keyword Planner search volumes × real top-of-page CPC,
 * times paid-CTR and competitive-share assumptions. A directional estimate,
 * NOT actual spend. Sharpens to "medium" confidence when the gated ad-spy layer
 * adds real creatives + landing pages.
 */
export interface SpendEstimate {
  currency: string;
  monthlyLow: number;
  monthlyMid: number;
  monthlyHigh: number;
  confidence: "low" | "medium";
  /** Keywords that carry a real CPC bid (the monetizable footprint). */
  commercialKeywords: number;
  /** Distinct ad destination URLs — a proxy for # of campaigns/landings (ad-spy only). */
  landingsDetected: number | null;
  /** Number of running creatives (ad-spy only). */
  activeCreatives: number | null;
  topSpendKeywords: SpendKeyword[];
  basis: string;
}

/** Roll-up of modeled spend across all competitors analyzed in a run. */
export interface SpendSummary {
  currency: string;
  monthlyLow: number;
  monthlyMid: number;
  monthlyHigh: number;
  competitorsEstimated: number;
  confidence: "low" | "medium";
  note: string;
}

/** Everything we learned about one competitor domain. */
export interface BenchmarkCompetitor {
  domain: string;
  source: "brand_profile" | "manual" | "derived";
  keywords: BenchmarkKeyword[];
  totalVolume: number;
  landing: CompetitorLanding | null;
  ads: CompetitorAd[] | null;
  adsStatus: AdsStatus;
  /** Modeled monthly Google Search investment (null when nothing is monetizable). */
  spend?: SpendEstimate | null;
  /** Non-fatal notes (e.g. "landing fetch timed out"). */
  notes: string[];
}

/** A keyword competitors are associated with that the brand appears to miss. */
export interface KeywordGap {
  text: string;
  avgMonthlySearches: number;
  competition: Competition;
  cpcLowMicros?: number;
  cpcHighMicros?: number;
  competitorsCovering: string[]; // domains whose footprint surfaced this kw
  brandCovers: boolean;
}

/**
 * Google Ads forecast for the recommended keyword set — Google's own traffic
 * projection so every recommendation carries real numbers ("run these keywords
 * → ~X clicks at ~€Y/mo"). Generated by GenerateKeywordForecastMetrics.
 *
 * Confidence split (honest framing): impressions / clicks / ctr / avgCpc / cost
 * are forecast directly by Google → reliable. conversions / conversionRate / cpa
 * assume an *estimated* typical conversion rate (we don't read the account's
 * conversion tracking), so they are directional, not promised.
 */
export interface ForecastProjection {
  currency: string; // display symbol (account currency)
  periodDays: number; // forecast window (≈30 → monthly)
  keywordCount: number;
  keywords: string[]; // the keywords that were forecast
  impressions: number;
  clicks: number;
  ctr: number; // 0..1
  avgCpcMicros: number;
  costMicros: number;
  conversions: number;
  conversionRate: number; // 0..1
  cpaMicros: number;
  maxCpcMicros: number; // the manual-CPC bid the forecast used
  basis: string;
}

/** The AI's synthesized strategy — the "so what / do this" layer. */
export interface BenchmarkStrategy {
  summary: string; // executive summary, in the brand's language
  positioning: string;
  opportunities: string[];
  threats: string[];
  recommendedKeywords: string[];
  recommendedAngles: string[];
}

export interface BenchmarkReport {
  generatedAt: string;
  language: string;
  country: string;
  /** Account currency display symbol (resolved from the Google Ads account). */
  currency: string;
  brand: { name: string; website: string | null; domain: string | null };
  brandKeywords: BenchmarkKeyword[];
  competitors: BenchmarkCompetitor[];
  keywordGaps: KeywordGap[];
  strategy: BenchmarkStrategy;
  /** Google's traffic forecast for the recommended plan (null if unavailable). */
  forecast?: ForecastProjection | null;
  /** Modeled combined competitor investment (null when nothing could be estimated). */
  spendSummary?: SpendSummary | null;
  meta: {
    liveAdSpy: boolean;
    domainsAnalyzed: number;
    keywordsDiscovered: number;
  };
}

/** Cost-attribution linkage threaded through every metered call in a run. */
export interface BenchmarkCostContext {
  userId?: string | null;
  brandId?: string | null;
  workspaceId?: string | null;
  runId?: string | null;
}
